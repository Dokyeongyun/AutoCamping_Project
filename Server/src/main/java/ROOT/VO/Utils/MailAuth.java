package ROOT.VO.Utils;

public class MailAuth {
    private String mailAuthId;
    private String mailAuthNum;
    private String mailAuthEmail;
    private String mailAuthDate;

    public String getMailAuthId() {
        return mailAuthId;
    }

    public void setMailAuthId(String mailAuthId) {
        this.mailAuthId = mailAuthId;
    }

    public String getMailAuthNum() {
        return mailAuthNum;
    }

    public void setMailAuthNum(String mailAuthNum) {
        this.mailAuthNum = mailAuthNum;
    }

    public String getMailAuthEmail() {
        return mailAuthEmail;
    }

    public void setMailAuthEmail(String mailAuthEmail) {
        this.mailAuthEmail = mailAuthEmail;
    }

    public String getMailAuthDate() {
        return mailAuthDate;
    }

    public void setMailAuthDate(String mailAuthDate) {
        this.mailAuthDate = mailAuthDate;
    }
}
